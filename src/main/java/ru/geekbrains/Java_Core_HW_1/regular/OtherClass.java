package ru.geekbrains.Java_Core_HW_1.regular;

/**
 * другой, очень полезный класс приложения. Здесь мы можем описать его основные назначения и
 * способы взаимодействия с нимм.
 */
public class OtherClass {

    /**
     * метод суммирования двух целых чисел
     * @param a первое число
     * @param b второе число
     * @return результат сложения
     */
    public static int add(int a, int b) {
        return a + b;
    }

    /**
     * метод деления двух целых чисел
     * @param a делимое
     * @param b делитель
     * @return частное
     */
    public static int div(int a, int b) {
        return a / b;
    }

    /**
     * метод умножения двух целых чисел
     * @param a первый множитель
     * @param b второй множитель
     * @return производное
     */
    public static int mul(int a, int b) {
        return a * b;
    }

    /**
     * метод для вычитания двух целых чисел
     * @param a первое число
     * @param b второе число
     * @return результат вычитания
     */
    public static int sub(int a, int b) {
        return a - b;
    }
}
